package com.tnsif.day12;

public class designpattern { 
	    public static void main(String[] args) {
	        
	        int rows = 3;
	        int columns = 3;
	        
	        
	        for (int i = 0; i < rows; i++) {  
	            for (int j = 1; j <= columns; j++) { 
	                System.out.print(j + " ");
	            }
	            System.out.println();  
	        }
	    }
	}



